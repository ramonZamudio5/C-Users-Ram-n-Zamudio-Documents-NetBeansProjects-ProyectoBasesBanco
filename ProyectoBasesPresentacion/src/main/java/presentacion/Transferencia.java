    private void btnTransActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTransActionPerformed
        // TODO add your handling code here:   
        try {
            Double monto = Double.valueOf(montoText.getText());
            LocalDateTime fecha = LocalDateTime.now();
            RetiroSinCuentaDTO retiro = new RetiroSinCuentaDTO(fecha, monto, idCuenta);
            int idCuentaDestino = Integer.parseInt(idCuentaText.getText());
            TransferenciaDTO transferencia = new TransferenciaDTO(fecha, monto, idCuenta, idCuentaDestino);
            control.realizarTransferencia(transferencia);
            control.mostrarMensajeTransferenciaExitosa(idCuenta);
            dispose();
        } catch (Exception ex) {
            control.mostrarErrorTransferencia();
        }
    }//GEN-LAST:event_btnTransActionPerformed

