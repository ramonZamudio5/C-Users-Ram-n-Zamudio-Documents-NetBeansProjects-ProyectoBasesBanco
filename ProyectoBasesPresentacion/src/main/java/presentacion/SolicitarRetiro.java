    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Double monto = Double.valueOf(jTextField1.getText());
            LocalDateTime fecha = LocalDateTime.now();
            RetiroSinCuentaDTO retiro = new RetiroSinCuentaDTO(fecha, monto, idCuenta);
            RetiroSinCuentaDTO retironuevo = control.agregarRetiroSinCuenta(retiro);
            control.mostrarMensajeSolicitudRetiroExitoso(retironuevo,idCuenta);
            dispose();
        } catch (Exception ex) {
            control.mostrarErrorSolicitudRetiro();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


